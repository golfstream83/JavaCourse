package ru.tuin;

public class Calculator {
	
	/**
	* adding two numbers
	**/
	public double add (double first, double second) {
		return first + second;
	}
	
	/**
	* Subtract from the number of first second
	**/
	public double substruct (double first, double second) {
		return first - second;
	}
	
	/**
	* first division of the number of second
	**/
	public double div (double first, double second) {
		return first / second;
	}
	
	/**
	* first multiplication of the number of second
	**/
	public double multiple (double first, double second) {
		return first * second;
	}
}